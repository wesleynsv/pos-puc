const lista = [10,20,30,40,50]; //Cria uma lista com os valores nos colchetes

console.log(lista);

lista.push(60,70); // Joga os valores no parenteses na pilha
console.log(lista); 

lista.pop(); // Remove o primeiro item da lista
console.log(lista);

// Tem que lembrar que essa é a mesma notação para a lista (array), 
// porém a forma como os valores são declarados é que vai determinar
// se estamos lidando com uma fila ou uma pilha



